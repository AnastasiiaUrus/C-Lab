#include <stdio.h> // Бібліотека для printf 
#include <math.h>

int main() {
    // ЗАВДАННЯ 1: Арифметичні вирази 
    int a = 2, b = 31;
    int c = 45, d = 54, e = 11;
    int f = 15, g = 4;
    double f_double = 15.0, g_double = 4.0;
    int h = 67, i = 5;
    double v1 = 2.0, v2 = 45.1, v3 = 3.2, v4 = 2.0;

    // Змінні для збереження результатів завдання 1
    int res1_int;
    double res1_double;

    printf("Task 1 Results\n");

    res1_int = a + b;
    printf("1) %d + %d = %d\n", a, b, res1_int);

    res1_int = c * d - e;
    printf("2) %d * %d - %d = %d\n", c, d, e, res1_int);

    res1_int = f / g;
    printf("3) %d / %d = %d (integer division)\n", f, g, res1_int);

    res1_double = f_double / g_double;
    printf("4) %.1f / %.1f = %.2f (double division)\n", f_double, g_double, res1_double);

    res1_int = h % i;
    printf("5) %d %% %d = %d\n", h, i, res1_int);

    res1_double = (v1 * v2 + v3) / v4;
    printf("6) (%.1f * %.1f + %.1f) / %.1f = %.2f\n", v1, v2, v3, v4, res1_double);

    // ЗАВДАННЯ 2

    float n1 = 1e-4f;                // 10^-4
    double n2 = 24.33e5;             // 24.33 * 10^5
    double p = M_PI;                 // Число Пі
    double exp_e = M_E;              // Число е
    double root5 = sqrt(5.0);        // Корінь з 5 (double для стабільного виводу)
    double ln100 = log(100.0);       // Натуральний логарифм

    printf("\nresult task 2:\n");

    printf("1) 10^-4   = %.4f\n", n1); 
    
    printf("2) 24.33E5 = %.2f\n", n2);
    printf("3) pi      = %.2f\n", p);
    printf("4) e       = %.2f\n", exp_e);
    printf("5) sqrt(5) = %.2f\n", root5);
    printf("6) ln(100) = %.2f\n", ln100);

    // ЗАВДАННЯ 3    
    int user_digit; // Змінна для збереження введеної цифри

    printf("\nTASK 3 RESULTS\n");
    printf("Введіть будь-яке ціле число: ");
    
    scanf("%d", &user_digit); 

    printf("results:\n"); 

    printf("%d)\n\n", user_digit);
    printf("- %d - %d - %d\n\n", user_digit, user_digit, user_digit);
    printf(" %d | %d | %d\n\n", user_digit, user_digit, user_digit);
    printf("- %d - %d - %d,\n", user_digit, user_digit, user_digit);

    // ЗАВДАННЯ 4: Обчислення сили притягання (Формула Ньютона)
    
    const double gamma = 6.674e-11; // Гравітаційна стала
    double m1, m2, r, F;

    printf("\nTASK 4 RESULTS\n");
    
    printf("Введіть масу першого тіла (кг): ");
    scanf("%lf", &m1);
    printf("Введіть масу другого тіла (кг): ");
    scanf("%lf", &m2);
    printf("Введіть відстань між тілами (м): ");
    scanf("%lf", &r);

    F = gamma * (m1 * m2) / (r * r);

    // 4. Виведення результату в науковому форматі (%e)
    printf("\nСила притягання F = %e Н (Ньютонів)\n", F);

    // ЗАВДАННЯ 5: Оптимізація множення 
    double x, res; 

    printf("\nTASK 5 RESULTS\n");
    printf("Введіть число x: ");
    scanf("%lf", &x);

    // а) x^4 за дві операції
    res = x * x;          // 1 (отримали x^2)
    res = res * res;      // 2 (отримали x^4)
    printf("a) x^4  = %.2f\n", res);

    // б) x^6 за три операції
    res = x * x;          // 1 (x^2)
    res = res * x;        // 2 (x^3)
    res = res * res;      // 3 (x^6)
    printf("б) x^6  = %.2f\n", res);

    // в) x^9 за чотири операції
    res = x * x;          // 1 (x^2)
    res = res * res;      // 2 (x^4)
    res = res * res;      // 3 (x^8)
    res = res * x;        // 4 (x^9)
    printf("в) x^9  = %.2f\n", res);

    // г) x^15 за п'ять операцій
    // Тут нам знадобиться одна проміжна змінна для x^3, щоб вкластися в 5 дій
    double x3 = x * x * x; // 2 операції (x^3)
    res = x3 * x3;         // 3 (x^6)
    res = res * res;       // 4 (x^12)
    res = res * x3;        // 5 (x^15)
    printf("г) x^15 = %.2f\n", res);

    // д) x^28 за шість операцій
    res = x * x;           // 1 (x^2)
    res = res * res;       // 2 (x^4)
    res = res * x * x * x; // 3, 4 (отримали x^7)
    res = res * res;       // 5 (x^14)
    res = res * res;       // 6 (x^28)
    printf("д) x^28 = %.2f\n", res);

    // е) x^64 за шість операцій
    res = x * x;           // 1 (x^2)
    res = res * res;       // 2 (x^4)
    res = res * res;       // 3 (x^8)
    res = res * res;       // 4 (x^16)
    res = res * res;       // 5 (x^32)
    res = res * res;       // 6 (x^64)
    printf("е) x^64 = %.2f\n", res);

    // ЗАВДАННЯ 6: Переведення Цельсія у Фаренгейта
    double celsius, fahrenheit;

    printf("\nTASK 6 RESULTS\n");

    printf("Введіть температуру в градусах Цельсія: ");
    scanf("%lf", &celsius);

    // Обчислення за формулою F = (9/5)*C + 32
    fahrenheit = (9.0 * celsius / 5.0) + 32;
    printf("F=%g\n", fahrenheit);

    // ЗАВДАННЯ 7: Ціла та дробова частини, округлення
    double x_val;

    printf("\nTASK 7 RESULTS\n");
    printf("Введіть дійсне число x: ");
    scanf("%lf", &x_val);
    int int_part_manual = (int)x_val;              // Примусове відкидання дробу
    double frac_part_manual = x_val - int_part_manual;


    double int_part_math = trunc(x_val);           // Ціла частина (відкидання)
    double frac_part_math = fmod(x_val, 1.0);      // Остача від ділення на 1
    double ceil_val = ceil(x_val);                 // Найменше ціле >= x
    double floor_val = floor(x_val);               // Найбільше ціле <= x
    double round_val = round(x_val);               // Математичне округлення

    printf("\n результати для x = %g:\n", x_val);
    printf("Ціла частина: ручна = %d, функція trunc = %.0f\n", int_part_manual, int_part_math);
    printf("Дробова частина: ручна = %f, функція fmod = %f\n", frac_part_manual, frac_part_math);
    printf("Найменше ціле >= x (ceil):  %.0f\n", ceil_val);
    printf("Найбільше ціле <= x (floor): %.0f\n", floor_val);
    printf("Округлене значення (round):  %.0f\n", round_val);

    // ЗАВДАННЯ 8: Різниця та добуток двох дійсних чисел
    double num_a, num_b;
    double diff, prod;

    printf("\nTASK 8 RESULTS\n");
    
    printf("Введіть перше дійсне число: ");
    scanf("%lf", &num_a);
    printf("Введіть друге дійсне число: ");
    scanf("%lf", &num_b);

    diff = num_a - num_b;
    prod = num_a * num_b;

    printf("\n результати:\n");
    printf("Різниця: %.2f\n", diff);
    printf("Добуток: %.2f\n", prod);

    // ЗАВДАННЯ 9: Середнє арифметичне та гармонічне
    double n1, n2;
    double arithmetic_mean, harmonic_mean;

    printf("\nTASK 9 RESULTS\n");
    printf("Введіть два числа через пробіл: ");
    scanf("%lf %lf", &n1, &n2);

    arithmetic_mean = (n1 + n2) / 2.0;

    harmonic_mean = (2.0 * n1 * n2) / (n1 + n2);

    printf("\n Результати:\n");
    printf("Середнє арифметичне: %.4f (науковий: %e)\n", arithmetic_mean, arithmetic_mean);
    printf("Середнє гармонічне:  %.4f (науковий: %e)\n", harmonic_mean, harmonic_mean);

    return 0;
}